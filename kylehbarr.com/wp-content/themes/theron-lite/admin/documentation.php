                <h4>Custom Button Shortcode</h4>
                
                <p>Write your button text and select the text then click the <span class="ctrl_shrtcd znbttn"></span> button. It will Produce this:<br /><br />
                
                [button url="http://www.google.com"]Button Text[/button]<br /><br />
				
                Change the value of the url from http://www.google.com to your own url.
                
                </p>
                
                
                
                <h4>Quote Shortcode</h4>
Click the quote <span class="znqot"></span> icon in the post editor to add quote.<br /><br />
[quote]Your quote.[/quote]
                
                    
                    
                   <h4>Facebook like Button</h4>
                   To add facebook like button click on the <span class="znfbthmb ctrl_shrtcd"></span>  button, this will add [fblike]
                   
                   <h4>Youtube Video Shortcode</h4>
                   Click on the <span class="znytb ctrl_shrtcd"></span> button to add youtube video.<br /><br />
                   [youtube width="640" height="385" video_id=""]<br /><br />
                   In width set the width and in height set the height of your video. The default is 640x385 . In video Id put your youtube video ID.
                   
                   <h4>Vimeo Video Shortcode</h4>
                   Click the <span class="znvmo ctrl_shrtcd"></span> button to add vimeo video.<br /><br />
					[vimeo width="640" height="385" video_id=""]<br /><br />
In width set the width and in height set the height of your video. The default is 640x385. In video Id put your youtube video ID.
                   
                                
