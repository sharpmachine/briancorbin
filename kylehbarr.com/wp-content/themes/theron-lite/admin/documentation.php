                <h4>Theron Widgets</h4>
                
                <div class="docum_widgets"></div>


<h4>1. Featured Posts</h4>
<p>Featured Posts displays specified number of posts from a specified category in your sidebars. The Options for this widget are:<br /><br />
<strong>Title:</strong> Set the title of the widget.<br /><br />
<strong>Category of Posts:</strong> Select the category of the featured posts<br /><br />
<strong>Number of Posts:</strong> Default is 3; increase the number if you want more than 3 posts to show up in the widget.</p>




<h4>2. Popular Posts</h4>
<p>Popular Posts widget displays number of popular posts in the sidebar. There are two options to set:<br /><br />
<strong>Title:</strong> Set the title of the widget.<br /><br />
<strong>Number of Posts:</strong> Default is 3; increase the number if you want more than 3 posts to show up in the widget.</p>



<h4>3. Random Posts</h4>
<p>Random Posts widget displays number of random posts in the sidebar. There are two options to set:<br /><br />
<strong>Title:</strong> Set the title of the widget.<br /><br />
<strong>Number of Posts:</strong> Default is 3; increase the number if you want more than 3 posts to show up in the widget.</p>




                

                
